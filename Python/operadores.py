# Aritmética
a = 10 + 5 * 2 - 8 / 4
print(f"a = {a}")

b = (10 + 5) * (2 - 8) / 4
print(f"b = {b}")

# Asignación
c = 10
print(f"c = {c}")

c += 5
print(f"c += 5 -> c = {c}")

c *= 2
print(f"c *= 2 -> c = {c}")

c /= 5
print(f"c /= 5 -> c = {c}")

# Comparación
d = 10 > 5
print(f"d = {d}")

e = 10 != 10
print(f"e = {e}")

f = "abc" == "abc"
print(f"f = {f}")

# Lógica
g = (10 > 5) and (7 < 12)
print(f"g = {g}")

h = (10 < 5) or (7 > 12)
print(f"h = {h}")

i = not (10 > 5)
print(f"i = {i}")
